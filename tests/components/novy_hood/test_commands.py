@pytest.mark.parametrize(
    ("command_cls", "expected"),
    [
        (NovyHoodPlus, EXPECTED_PLUS),
        (NovyHoodMinus, EXPECTED_MINUS),
        (NovyHoodLight, EXPECTED_LIGHT),
        (NovyHoodPower, EXPECTED_POWER),
    ],
)
def test_command_timings(
    command_cls: type[RadioFrequencyCommand], expected: list[int]
) -> None:
    """Each command emits the canonical timing sequence."""
    command = command_cls()
    assert command.frequency == 433_920_000
    assert command.modulation is ModulationType.OOK
    assert command.repeat_count == 8
    assert command.get_raw_timings() == expected
