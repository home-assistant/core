"""Tests for the swagger-generated v2 telemetry models.

The companion module ``_telemetry_models.py`` is generated by
``datamodel-code-generator`` from ABRP's v2 OpenAPI spec and checked into
the repo (no runtime codegen dep). These tests pin the **keep-set** the
integration actually consumes — every leaf reachable from ``OutputPoint``
via the sensor + tracker value_fn registry, plus the exact cardinality
of ``OutputPoint``'s surfaced fields — so a swagger drift, accidental
hand-edit, or sloppy regen-without-prune fails fast.

The ``OutputPoint`` cardinality assertion below doubles as the
swagger-drift canary referenced by ``_telemetry_models.py``'s
regen-then-prune header: if a future regen re-introduces a pruned field
and the prune step is forgotten, this test goes loudly RED rather than
the integration silently shipping ~3k LOC of generated noise.

Parametrize floor is one row per surfaced leaf (12 entries) —
representative-shape would blind the suite to per-leaf wire-key drift
(e.g. codegen renaming ``frac`` to ``percent`` on ``Soh`` only).
"""

from typing import get_type_hints, is_typeddict

import pytest

from homeassistant.components.abetterrouteplanner._telemetry_models import (
    BatteryCapacity,
    BatteryTemperature,
    CalibratedRefCons,
    ChargingState,
    EstimatedBatteryRange,
    Location,
    Odometer,
    OutputPoint,
    OutputPointWithVehicleId,
    Power,
    Soc,
    Soe,
    Soh,
    Voltage,
)

# Wire-key keep-set: every leaf TypedDict that ``STAMPED_VALUE_FNS``
# (sensor) or the tracker ``LOCATION_VALUE_FN`` reads. ``Location``
# carries two axes (``lat`` + ``long``); every other leaf carries one.
_KEEP_SET_LEAVES: tuple[tuple[type, frozenset[str]], ...] = (
    (Soc, frozenset({"frac"})),
    (Power, frozenset({"w"})),
    (Voltage, frozenset({"v"})),
    (Soe, frozenset({"wh"})),
    (Odometer, frozenset({"m"})),
    (CalibratedRefCons, frozenset({"wh_per_km"})),
    (BatteryCapacity, frozenset({"wh"})),
    (Soh, frozenset({"frac"})),
    (EstimatedBatteryRange, frozenset({"m"})),
    (BatteryTemperature, frozenset({"c"})),
    (ChargingState, frozenset({"state"})),
    (Location, frozenset({"lat", "long"})),
)

# The 12 surfaced keys on ``OutputPoint`` after pruning. Anything other
# than this exact set is a drift signal: extra keys = generator
# re-introduced a pruned field (regen-without-prune); missing keys =
# accidental over-prune dropped a surfaced field.
_EXPECTED_OUTPUT_POINT_KEYS: frozenset[str] = frozenset(
    {
        "soc",
        "power",
        "voltage",
        "soe",
        "odometer",
        "calibratedRefCons",
        "batteryCapacity",
        "soh",
        "estimatedBatteryRange",
        "batteryTemperature",
        "chargingState",
        "location",
    }
)


def test_output_point_is_typeddict() -> None:
    """``OutputPoint`` is a TypedDict (not a dataclass or NamedTuple)."""
    assert is_typeddict(OutputPoint)


def test_output_point_with_vehicle_id_is_typeddict() -> None:
    """``OutputPointWithVehicleId`` is a TypedDict."""
    assert is_typeddict(OutputPointWithVehicleId)


@pytest.mark.parametrize(
    ("leaf_cls", "expected_keys"),
    [pytest.param(leaf, keys, id=leaf.__name__) for leaf, keys in _KEEP_SET_LEAVES],
)
def test_keep_set_leaf_typeddict(leaf_cls: type, expected_keys: frozenset[str]) -> None:
    """Each surfaced leaf is a TypedDict carrying its expected wire key(s).

    Parametrize floor is one row per surfaced leaf so a per-leaf
    wire-key drift (e.g. codegen renaming ``frac`` to ``percent`` on
    ``Soh`` only) goes RED — the matching value_fn closure would
    otherwise return ``None`` silently.
    """
    assert is_typeddict(leaf_cls)
    assert expected_keys <= get_type_hints(leaf_cls).keys()


def test_output_point_cardinality_pins_surfaced_keys() -> None:
    """``OutputPoint`` exposes EXACTLY the 12 surfaced wire keys.

    Doubles as a swagger-drift canary per the regen-then-prune header in
    ``_telemetry_models.py``: if a future regen forgets the prune step,
    cardinality jumps from 12 to ~27 and this assertion goes RED.
    """
    assert set(get_type_hints(OutputPoint)) == set(_EXPECTED_OUTPUT_POINT_KEYS)


def test_output_point_with_vehicle_id_extends_with_vehicle_id() -> None:
    """``OutputPointWithVehicleId`` adds ``vehicleId`` on top of ``OutputPoint``."""
    assert set(get_type_hints(OutputPointWithVehicleId)) == (
        set(_EXPECTED_OUTPUT_POINT_KEYS) | {"vehicleId"}
    )
